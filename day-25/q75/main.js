"use strict";
let x = 4;
console.log("Starting value of x:", x);
/* We simplify arithmetic operator on x using
compound assignment operators, like a shortcut.*/
x += 3; // Adds 3 to x
console.log("After adding 3:", x);
x -= 2; // Subtracts 2 from x
console.log("After subtracting 2:", 2);
x *= 5; // Multiplies x by 5
console.log("After multiplication by 5:", x);
x /= 2; // Divides x by 2
console.log("After dividing by 2:", x);
