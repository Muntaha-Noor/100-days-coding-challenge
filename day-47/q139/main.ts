// Reserved word: for - used to repeatedly execute a  block of code as long as a condition is true
let sum = 0;
for (let i = 1; i <= 5; i++) {
  sum += i;
}

// Reserved word: console - used to log information to the console
console.log("Sum of number from 1 to 5:", sum);

// Reserved word: return - used to exit a function and return a value from that function
function calculateRectangleArea(length: number, width: number) {
  return length * width;
}

